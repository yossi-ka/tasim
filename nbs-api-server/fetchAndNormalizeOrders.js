const getAmountDaysToImport = async () => {
    try {
        console.log('\nÔøΩ Getting last import date from server...');
        const response = await axios.get(
            'https://us-central1-kanfei-nesharim2.cloudfunctions.net/lastOrderImportDate',
            {
                headers: {
                    'Content-Type': 'application/json'
                },
                timeout: 30000 // 30 seconds timeout
            }
        );

        if (response.status !== 200 || !response.data.lastImportDate) {
            console.log('‚ö†Ô∏è Could not get last import date, defaulting to 1 day');
            return 1;
        }

        // ◊î◊û◊®◊™ ◊î◊™◊ê◊®◊ô◊ö ◊©◊î◊™◊ß◊ë◊ú ◊ú◊ê◊ï◊ë◊ô◊ô◊ß◊ò Date
        const lastImportDate = new Date(response.data.lastImportDate);
        const currentDate = new Date();

        // ◊ó◊ô◊©◊ï◊ë ◊î◊î◊§◊®◊© ◊ë◊ô◊û◊ô◊ù
        const timeDifference = currentDate - lastImportDate;
        const daysDifference = timeDifference / (1000 * 60 * 60 * 24);

        // ◊¢◊ô◊í◊ï◊ú ◊õ◊ú◊§◊ô ◊û◊¢◊ú◊î
        const daysToImport = Math.ceil(daysDifference);

        console.log(`üìÖ Last import: ${lastImportDate.toISOString()}`);
        console.log(`üìÖ Current date: ${currentDate.toISOString()}`);
        console.log(`üìä Days to import: ${daysToImport}`);

        // ◊ï◊ô◊ì◊ï◊ê ◊©◊î◊¢◊®◊ö ◊ú◊ê ◊ô◊î◊ô◊î ◊ß◊ò◊ü ◊û-1
        return Math.max(daysToImport, 1);

    } catch (error) {
        console.error('‚ùå Error getting last import date:', error.message);
        console.log('‚ö†Ô∏è Defaulting to 1 day import');
        return 1;
    }
};

